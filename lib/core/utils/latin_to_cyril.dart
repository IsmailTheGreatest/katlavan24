String toUzbCyril(String input) {
  final map = {
    'A': 'А', 'B': 'Б', 'D': 'Д', 'E': 'Э', 'F': 'Ф', 'G': 'Г',
    'H': 'Ҳ', 'I': 'И', 'J': 'Ж', 'K': 'К', 'L': 'Л', 'M': 'М',
    'N': 'Н', 'O': 'О', 'P': 'П', 'Q': 'Қ', 'R': 'Р', 'S': 'С',
    'T': 'Т', 'U': 'У', 'V': 'В', 'X': 'Х', 'Y': 'Й', 'Z': 'З',

    'a': 'а', 'b': 'б', 'd': 'д', 'e': 'э', 'f': 'ф', 'g': 'г',
    'h': 'ҳ', 'i': 'и', 'j': 'ж', 'k': 'к', 'l': 'л', 'm': 'м',
    'n': 'н', 'o': 'о', 'p': 'п', 'q': 'қ', 'r': 'р', 's': 'с',
    't': 'т', 'u': 'у', 'v': 'в', 'x': 'х', 'y': 'й', 'z': 'з',

    'O‘': 'Ў', 'o‘': 'ў', 'G‘': 'Ғ', 'g‘': 'ғ', 'Sh': 'Ш', 'sh': 'ш',
    'Ch': 'Ч', 'ch': 'ч', 'Ng': 'Нг', 'ng': 'нг', 'Ya': 'Я', 'ya': 'я',
    'Yo': 'Ё', 'yo': 'ё', 'Yu': 'Ю', 'yu': 'ю', 'Ts': 'Ц', 'ts': 'ц',
    'ʼ': 'ъ', '\'': 'ъ'
  };

  final digraphs = {
    'Oʻ': 'Ў', 'oʻ': 'ў', 'Gʻ': 'Ғ', 'gʻ': 'ғ',
    'Sh': 'Ш', 'sh': 'ш', 'Ch': 'Ч', 'ch': 'ч',
    'Ya': 'Я', 'ya': 'я', 'Yu': 'Ю', 'yu': 'ю',
    'Yo': 'Ё', 'yo': 'ё', 'Ts': 'Ц', 'ts': 'ц'
  };

  digraphs.forEach((latin, cyrillic) {
    input = input.replaceAll(latin, cyrillic);
  });

  return input.split('').map((c) => map[c] ?? c).join();
}
